    public void DtoB (int input){

    String Output = "";
    int binary[] = new int[40];
    int i = 0;

    while(input != 0){
        binary[i] = input%2;
        input = input/2;
        i++;
    }

    for (int k = i - 1; k >= 0; k--){
        Output = Output + binary[k];
    }
    lblDtoBAns.setText(Output);
    }
